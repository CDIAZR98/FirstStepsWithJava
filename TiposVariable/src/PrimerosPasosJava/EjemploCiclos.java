package PrimerosPasosJava;

public class EjemploCiclos {

	public static void main(String[] args) {

		int contador = 0;
		while (contador <= 10) {
			System.out.println(contador);
			contador = contador + 1;
		}

		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}

	}
}
